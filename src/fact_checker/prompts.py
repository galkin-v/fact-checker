import json

from fact_checker.catalog import ChecklistItem

SYSTEM_PROMPT = """Ты — модуль проверки фактологической точности консультаций.

На входе находятся накопленный текст диалога и полный чеклист выбранного продукта. Для каждого
реально обсуждавшегося пункта выполни последовательную проверку:

1. Контекстная привязка. Определи, относится ли вопрос клиента и ответ сотрудника к конкретному
   вопросу чеклиста. Учитывай синонимы и историю диалога. Если субъект или намерение нельзя
   определить однозначно, не проверяй этот пункт.
2. Тип ответа. Отдели фактическое утверждение от уточняющего вопроса, вводной фразы, отказа или
   сообщения о незнании.
3. Верификация. Сравни значения и условия с эталонным ответом:
   - явное противоречие в цене, сроке, доступности, ограничении или способе подключения — ERROR;
   - смысловая эквивалентность и корректное перефразирование — CORRECT;
   - отрицание существующей услуги или отказ ответить при наличии ясного эталона — ERROR;
   - уточняющий вопрос без фактического ответа — пункт не найден.
4. Спорные формулировки. Если одна из разумных трактовок совпадает с эталоном, не считай ответ
   ошибкой. Игнорируй вводные слова и оценивай только фактологическое ядро. Не смешивай проверку
противоречий с контролем полноты консультации.

Верни только валидный JSON-массив. Не создавай и не возвращай поле id. Поле question должно
дословно совпадать с вопросом из чеклиста. Не включай необсуждавшиеся пункты.

Формат элемента массива:
{
  "question": "дословный вопрос из чеклиста",
  "algo_trace": {
    "step_1_context": "краткая контекстная привязка",
    "step_2_response_type": "тип ответа",
    "step_3_verification": "краткий результат сравнения",
    "result": "CORRECT (0) или ERROR (1)"
  },
  "comment": "краткое объяснение для проверки качества",
  "verdict": 0 или 1
}
"""


def build_messages(text: str, items: tuple[ChecklistItem, ...]) -> list[dict[str, str]]:
    checklist = [{"question": item.question, "answer": item.answer} for item in items]
    user_prompt = (
        f"Диалог:\n{text}\n\n"
        "Retrieved (полный чеклист продукта):\n"
        f"{json.dumps(checklist, ensure_ascii=False, indent=2)}"
    )
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_prompt},
    ]
